<?php

declare(strict_types=1);

namespace App\Messaging\Contracts;

use App\Models\Notification;
use RuntimeException;

/**
 * Контракт публикации уведомлений в брокер сообщений.
 *
 * Скрывает от HTTP-слоя (и любых других вызывающих сторон) детали
 * выбора брокера и маршрутизации:
 *
 *  - какие именно exchange/queues используются,
 *  - как `priority` уведомления маппится в numeric AMQP priority
 *    или в выбор конкретной очереди,
 *  - как настраивается persistence сообщений и publisher confirms,
 *  - какие connection/channel переиспользуются между вызовами.
 *
 * Все эти решения — забота реализации. На уровне контракта мы
 * фиксируем только семантику «сообщение должно оказаться в брокере
 * для последующей доставки воркером».
 *
 * Ожидаемые реализации:
 *
 *  - `App\Messaging\Rabbit\RabbitPublisher` — продовая, через php-amqplib;
 *  - возможный in-memory publisher для интеграционных тестов, не
 *    требующих живого RabbitMQ.
 *
 * Гарантия доставки — at-least-once на уровне брокера: Publisher
 * гарантирует, что сообщение хотя бы один раз окажется в очереди.
 * Дедупликация дублей (если повторно publish'нут тот же `Notification`)
 * обеспечивается на уровне Consumer'а — через UUID-первичный ключ
 * и проверку статуса в одной транзакции с обновлением; повторная
 * доставка того же id из RabbitMQ становится no-op.
 */
interface PublisherInterface
{
    /**
     * Публикует одно уведомление в брокер. Приоритет берётся из
     * `$notification->priority`; реализация сама решает, во что
     * именно его перевести (отдельная очередь high/low или
     * numeric AMQP priority в одной priority-queue).
     *
     * Метод **не сохраняет** уведомление в БД — предполагается, что
     * `Notification` уже персистирован вызывающей стороной до вызова
     * publish: id записи и будет ключом exactly-once на бизнес-уровне.
     *
     * Семантически возвращает `void` при успехе. Любая ошибка
     * транспорта (нет соединения с брокером, отказ в подтверждении
     * publish'а и т.п.) выражается исключением: на этом уровне
     * абстракции у нас нет понятия «частичный успех».
     *
     * @param Notification $notification Сохранённая в БД запись уведомления; её `id` и `priority` используются для публикации.
     *
     * @throws RuntimeException При невозможности доставить сообщение в брокер. Конкретный класс исключения определяется реализацией.
     */
    public function publish(Notification $notification): void;
}
