<?php

declare(strict_types=1);

namespace App\Enums;

/**
 * Статус доставки уведомления.
 *
 * Backed string enum: значения backing совпадают с тем, что хранится в БД
 * в поле `notifications.status` (см. миграцию 2026_05_18_120002, дефолтное
 * значение `queued`).
 *
 * Граф переходов жизненного цикла:
 *
 *     QUEUED ──► SENT ──► DELIVERED   (счастливый путь)
 *        │         │
 *        │         └────► FAILED      (провайдер принял, но позже отбил callback'ом)
 *        │
 *        └────────────► FAILED        (исчерпаны попытки до успешной передачи провайдеру)
 *
 * Терминальные статусы — DELIVERED и FAILED: запись уже не меняется
 * ни воркером, ни вебхуком. Идемпотентность обработки на бизнес-уровне
 * обеспечивается тем, что Consumer перед вызовом провайдера проверяет
 * текущий статус в одной транзакции с обновлением — повторная доставка
 * того же сообщения из RabbitMQ становится no-op.
 *
 * Сам граф переходов в enum'е не реализован (метод `canTransitionTo()`
 * и т.п.) — пока единственный потребитель ограничен Consumer'ом
 * и WebhookController'ом, и валидация переходов уместнее в их коде,
 * где доступен контекст транзакции.
 *
 * Используется:
 *
 *  - Eloquent-моделью Notification — как cast атрибута
 *    (`'status' => NotificationStatusEnum::class`);
 *  - Consumer'ом — для проверки текущего статуса перед отправкой
 *    и обновления после успеха/провала;
 *  - WebhookController'ом — для перевода SENT → DELIVERED/FAILED
 *    по callback'у провайдера;
 *  - JSON-resource'ом истории уведомлений подписчика.
 */
enum NotificationStatusEnum: string
{
    /**
     * Запись принята API и поставлена в очередь RabbitMQ. Дефолтное
     * состояние при создании. Ожидает обработки Consumer'ом.
     */
    case QUEUED = 'queued';

    /**
     * Provider::send() вернул успех; сообщение передано шлюзу.
     * Заполнен `provider_message_id`, выставлен `sent_at`. Ожидает
     * подтверждения доставки через webhook.
     */
    case SENT = 'sent';

    /**
     * Терминальный статус. Провайдер callback'ом подтвердил доставку
     * конечному получателю; выставлен `delivered_at`.
     */
    case DELIVERED = 'delivered';

    /**
     * Терминальный статус. Финальный провал: либо Consumer исчерпал
     * попытки до успешной передачи провайдеру, либо провайдер
     * callback'ом сообщил о невозможности доставки. Выставлен
     * `failed_at`; текст последней ошибки — в `last_error`.
     */
    case FAILED = 'failed';
}
